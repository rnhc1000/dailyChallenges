<?php 
/**
 * Anatomia de uma classse
 * class Nome { corpo }
 * O corpo é delimitado por chaves e possui
 * as seguintes características:
 * {
 *      atributos;
 *      comportaments; 
 * }
 * Ex: TV é um objeto que pode ser definido através do uso
 * de uma classe pois uma TV possui:
 * atributos: cor, marca, tamanho, peso, tecnologia
 * comportamentos: ligar, sintonizar, desligar
 * 
 * A classe é uma estrutura que vai personalizar os objetos a serem  
 * utilizados
 * Atributo -> Dados
 * Comportamento -> Métodos(funções)
 * A Classe define um tipo!
 * A Classe representa uma abstração o que leva a uma simplificação
 * O paradigma de orientação a objetos se baseia numa visão do mundo real
 * onde existem objetos e a interação entre eles.
 * A Classe é uma estrutura de dados
 * Ao criar um dado a partir de uma classe é denominado objeto. 
 * Assim, vc poe criar dados distintos a partir da uma mesma classe
 * 
 * Assim ao instanciar ua classe, cria-se um objeto
 * [ Classe ] ----> [ Instancia 1 ] -> [ Objeto 1 ]
 * [ Classe ] ----> [ Instancia 2 ] -> [ Objeto 2 ]
 * [ Classe ] ----> [ Instancia n ] -> [ Objeto n ]
 * Classe === (Molde)...... e a partir do (Molde) cria-se milhares 
 * de objetos.
 * Uma classe possui atributos e comportamentos, respectivamente
 * dados e métodos.
 * Um método especial é chamado construtor vuaj principal finalidade
 * é a de contruir objetos
 * Classe Produto {
 * Nome
 * Preço
 * Desconto
 * }
 * Classe Produto -> Construtor -> Objeto 1 ( Notebook, R$ 4200.00 15% )
 * Classe Produto -> Construtor -> Objeto 2 ( Celular, R$ 1800.00, 15%)
 * 
 */

